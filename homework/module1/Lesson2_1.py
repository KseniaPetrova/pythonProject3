
'''
Задача «Сложная сдача».

Описание: Одна из задач, в которой лучше исключить человеческий фактор, — подсчёт сдачи.
Определите, какую сдачу нужно выдать тому, кто купил 4,5кг черешни по цене 34 руб/кг
'''

price = 34  # рублей за кг
weight = 4.5  # вес в кг
pay = 500  # предположим заплатили 500р
change = float(pay) - float(price) * weight
print(change)
'''
Задача «Сдача всем».

Описание: Кроме ягод в магазине продаётся множество других товаров, которые продаются на развес. 
Давайте автоматизируем расчёт сдачи и для них!
'''


# Задача "Сложная сдача"
def calculate_change(price_per_kg, weight, amount_paid):
    total_cost = price_per_kg * weight
    change = amount_paid - total_cost
    return change


# Пример использования
price_per_kg = 34  # Цена за 1 кг черешни
weight = 4.5  # Вес покупки
amount_paid = 200  # Внесенная сумма

change = calculate_change(price_per_kg, weight, amount_paid)
print(f"Сдача составляет: {change:.2f} руб.")


# Задача "Сдача всем"
def calculate_change_for_item(price_per_kg, weight, amount_paid):
    total_cost = price_per_kg * weight
    change = amount_paid - total_cost
    return change


# Пример использования
items = [
    {"name": "Черешня", "price_per_kg": 34, "weight": 4.5, "amount_paid": 200},
    {"name": "Яблоки", "price_per_kg": 25, "weight": 2.3, "amount_paid": 100},
    {"name": "Апельсины", "price_per_kg": 40, "weight": 3.0, "amount_paid": 150}
]

for item in items:
    change = calculate_change_for_item(item["price_per_kg"], item["weight"], item["amount_paid"])
    print(f"Сдача за {item['name']} составляет: {change:.2f} руб.")

